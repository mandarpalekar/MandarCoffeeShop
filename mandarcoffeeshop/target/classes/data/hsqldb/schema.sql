DROP TABLE Product IF EXISTS;
CREATE TABLE Product (
 idp BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
 name VARCHAR(100) NOT NULL,
 price NUMERIC(10,2) NOT NULL,
 PRIMARY KEY (idp)
);

DROP TABLE Customer IF EXISTS;
CREATE TABLE Customer (
 idc BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
 fname VARCHAR(100) NOT NULL,
 lname VARCHAR(100) NOT NULL,
 PRIMARY KEY (idc)
);

DROP TABLE Orders IF EXISTS;
CREATE TABLE Orders (
 ido BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
 total NUMERIC(10,2) NOT NULL,
 PRIMARY KEY (ido)
);